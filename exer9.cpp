// Program to explain
// Relational Operators ( <, >, <=, >=, ==, != )  // TRUE OR FALSE  0 1
#include <iostream>
using namespace std;
int main()
{
	int n;
	cout << "\n Enter any Number : ";
	cin >> n;
	cout << "\n (n < 10) is = " << (n < 10); 
	cout << "\n (n > 10) is = " << (n > 10);
	cout << "\n (n <= 10) is = " << (n <= 10);
	cout << "\n (n >= 10) is = " << (n >= 10);
	cout << "\n (n == 10) is = " << (n == 10);
	cout << "\n (n != 10) is = " << (n != 10);
	return 0;
}

